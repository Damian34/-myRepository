    public void AddOrRemoveItem(int t1)//jesli t1=0 to dodaje jesli t1=1 to odejmuje ,a jesli t1=2 to sprzedaje
    {
        try{
        Mongo m = new Mongo("LocalHost",27017);
        
        DB db=m.getDB("MyDB");
        DBCollection col=db.getCollection("Item");
            
        ////
        BasicDBObject query = new BasicDBObject();
        query.put("_id", this.id_I);
        DBCursor c=col.find(query);
        DBObject s=c.next();
        Map<String,Object> map = s.toMap();
                
        double sp=(double)Double.valueOf(map.get("dostepne").toString());
        
        double p=0;
        try{
            p=Double.valueOf(this.jTextField1.getText());
        }catch(Exception e){}
        
        double k=p;
        
        if(t1==0)
        {
            sp+=p;            
        }
        else
        {
            //tzn jesli t1=1 lub 2                        
            if(sp<p)
            {
                k=sp;
                sp=0;
            }
            else{sp-=p;}            
        }        
        
        this.jLabel5.setText(sp+"");
        
        ////update liczbe dostepnych przedmiotow
        List<String> list=new ArrayList<>();
        list.addAll(s.keySet());               
        
        BasicDBObject query1 = new BasicDBObject();
        
        for(int i=0;i<list.size();i++)
        {
            query1.put(list.get(i),map.get(list.get(i)));
        }
        query1.put("dostepne", sp);
        
        /////
        if(t1==2)//to dodaje jeszcze do sprzedanych
        {
        double sp2=(double)Double.valueOf(map.get("sprzedane").toString());
        sp2+=k;
        this.jLabel4.setText(sp2+"");
        
        query1.put("sprzedane", sp2);
        }
        ////
        
        BasicDBObject query2 = new BasicDBObject();
        query2.append("_id", this.id_I);
        
        col.update(query2, query1);
                            
        }catch(Exception e){System.out.println("error: "+e);}
    }
    
