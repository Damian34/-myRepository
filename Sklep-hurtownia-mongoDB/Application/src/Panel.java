    public String format(String str)
    {
        String ss="";
        int l=12;
        
        ss+="<html>";
        int i;
        for(i=0;i<str.length();i++)
        {
            ss+=str.charAt(i);
            if(i%l==0 && i!=0)
            {ss+="<br>";}
        }
        ss+="</html>";
        
        return ss;        
    }
    
